package Models.amine.Personnel;

public sealed abstract class Personne
    permits Membre, Coach, Manager {

    protected int id;
    protected String nom;
    protected String prenom;
    protected String email;
    protected String telephone;

    public Personne(int id, String nom, String prenom, String email, String telephone) {
        this.id = id;
        this.nom = nom;
        this.prenom = prenom;
        this.email = email;
        this.telephone = telephone;
    }

    public int getId() { return id; }
    public void setId(int id) { this.id = id; }

    public String getNom() { return nom; }
    public void setNom(String nom) { this.nom = nom; }

    public String getPrenom() { return prenom; }
    public void setPrenom(String prenom) { this.prenom = prenom; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getTelephone() { return telephone; }
    public void setTelephone(String telephone) { this.telephone = telephone; }

    public abstract void afficherInfo();

    @Override
    public String toString() {
        return nom + " " + prenom + " (" + email + ")";
    }
}
